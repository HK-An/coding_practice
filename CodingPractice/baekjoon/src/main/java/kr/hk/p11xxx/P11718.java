package kr.hk.p11xxx;

import java.util.Scanner;

/*
* https://www.acmicpc.net/problem/11718
* #구현 #문자열
* */
public class P11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (sc.hasNext()) {
            System.out.println(sc.nextLine());
        }
    }
}
